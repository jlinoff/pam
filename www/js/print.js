export function enablePrinting() {
    // Use Bootstrap's d-none class rather than inline display:none &mdash;
    // Bootstrap dropdown CSS overrides inline styles when the menu opens.
    let eps = document.body.xGetN('.x-print')
    if ( window.prefs.enablePrinting ) {
        eps.forEach( (el) => { el.classList.remove('d-none') } )
    } else {
        eps.forEach( (el) => { el.classList.add('d-none') } )
    }
}

export function printRecords() {
    statusBlip(`generating print document...`)
    let html = genRecordsDocument()

    // Derive the CSS URL relative to the page base so it works on GitHub
    // Pages and other sub-path deployments (window.location.origin alone
    // gives the wrong path there).
    let base = document.querySelector('base')
    let cssBase = base ? base.href : window.location.href.replace(/\/[^\/]*$/, '/')
    let cssUrl = new URL('css/print-report.css', cssBase).href
    html = html.replace('href="" id="x-print-report-css"',
                        `href="${cssUrl}" id="x-print-report-css"`)

    // Detect iPadOS — it uses the native AirPrint sheet which snapshots
    // iframe content before onload fires, producing a blank page.
    // All other platforms use the iframe path which handles CSP correctly.
    let isIpadOS = /iPad/.test(navigator.userAgent) ||
        (navigator.platform === 'MacIntel' && navigator.maxTouchPoints > 1)

    // E2E test hook — always uses iframe so the hook receives a real DOM
    // element.  Production code uses the appropriate path below.
    if (typeof window._pamPrintHook === 'function') {
        let iframe = document.createElement('iframe')
        iframe.style.cssText = 'position:fixed;top:-9999px;left:-9999px;' +
                               'width:210mm;height:297mm;border:none;'
        document.body.appendChild(iframe)
        let idoc = iframe.contentDocument || iframe.contentWindow.document
        idoc.open()
        iframe.contentWindow.onload = function() {
            window._pamPrintHook(iframe)
            document.body.removeChild(iframe)
        }
        idoc.write(html)
        idoc.close()
        return
    }

    if (isIpadOS) {
        // iPadOS path: window.open() — AirPrint handles a new window
        // correctly but snapshots iframe content before onload fires.
        let win = window.open('', '_blank')
        if (!win) {
            statusBlip('Pop-up blocked — please allow pop-ups and try again.')
            return
        }
        win.document.open()
        win.document.write(html)
        win.document.close()
        win.focus()
        // Poll for the CSS link to finish loading, then print.
        // link.onload is unreliable after document.write() in some browsers.
        let link = win.document.getElementById('x-print-report-css')
        let attempts = 0
        let poll = setInterval(function() {
            attempts++
            let loaded = !link || link.sheet !== null
            if (loaded || attempts > 20) {
                clearInterval(poll)
                win.print()
                win.onafterprint = function() { win.close() }
            }
        }, 100)
        return
    }

    // Desktop / iPhone path: hidden iframe shares the parent origin so
    // the linked stylesheet is permitted by CSP style-src 'self'.
    let iframe = document.createElement('iframe')
    iframe.style.cssText = 'position:fixed;top:-9999px;left:-9999px;' +
                           'width:210mm;height:297mm;border:none;'
    function triggerPrint() {
        iframe.contentWindow.focus()
        iframe.contentWindow.print()
        iframe.contentWindow.onafterprint = function() {
            document.body.removeChild(iframe)
        }
    }
    document.body.appendChild(iframe)
    let idoc = iframe.contentDocument || iframe.contentWindow.document
    idoc.open()
    iframe.contentWindow.onload = triggerPrint
    idoc.write(html)
    idoc.close()
}

// The new Sanitizer API is not yet widely available.
function sanitize(html) {
    return html.replaceAll('&', '&amp;')
        .replaceAll('<', '&lt;')
        .replaceAll('>', '&gt;')
        .replaceAll("'", '&apos;')
        .replaceAll('"', '&quot;')
}

function genRecordsDocument() {
    let recordsContainer = document.body.xGet('#records-accordion')
    if (!recordsContainer) {
        return '<h4>No records</h4>'
    }
    let accordionItems = recordsContainer.xGetN('.accordion-item')
    if (!accordionItems) {
        return '<h4>No records</h4>'
    }
    if (accordionItems.length === 0) {
        return '<h4>No records available</h4>'
    }

    // Count visible records.
    let count = 0
    for (let i=0; i<accordionItems.length; i++) {
        if (!accordionItems[i].classList.contains('d-none')) {
            count += 1
        }
    }
    if (count === 0) {
        return '<h4>No records selected</h4>'
    }

    let search = document.getElementById('search').value
    let now = new Date().toLocaleString('en-US', {
        year: 'numeric', month: 'long', day: 'numeric',
        hour: '2-digit', minute: '2-digit'
    })

    // Cover block meta line
    let metaParts = [`${count} record${count !== 1 ? 's' : ''}`]
    if (search) {
        metaParts.push(`filtered by &ldquo;${sanitize(search)}&rdquo;`)
    }
    if (window.prefs.fileName) {
        metaParts.unshift(`File: ${sanitize(window.prefs.fileName)}`)
    }
    let metaLine = metaParts.join(' &nbsp;&middot;&nbsp; ')

    // Build record cards HTML
    let cards = ''
    for (let i=0; i<accordionItems.length; i++) {
        let accordionItem = accordionItems[i]
        if (accordionItem.classList.contains('d-none')) {
            continue
        }
        let button = accordionItem.xGet('.accordion-button')
        let title = button.innerHTML

        // Collect non-empty fields
        let fieldRows = ''
        let rows = accordionItem.xGetN('.row')
        for (let j=0; j<rows.length; j++) {
            let row = rows[j]
            let nameDiv = row.xGet('.x-fld-name')
            if (!nameDiv) {
                continue // button row
            }
            let valueDiv = row.xGet('.x-fld-value')
            let type = valueDiv.getAttribute('data-fld-type')
            let value = valueDiv.innerHTML
            switch (type) {
            case 'html':
            case 'password':
            case 'textarea':
            case 'url':
                value = valueDiv.getAttribute('data-fld-raw-value')
                break
            default:
                break
            }

            // Skip empty fields
            if (!value || !value.trim()) {
                continue
            }

            let name = sanitize(nameDiv.innerHTML)
            if (type !== 'html') {
                value = sanitize(value)
            }
            let pwStyle = (type === 'password') ? 'color:#1a3a6e;' : ''
            fieldRows += `
              <tr>
                <td class="fn">${name}</td>
                <td class="fv" style="${pwStyle}">${value}</td>
              </tr>`
        }

        // Skip records where every field was empty
        if (!fieldRows) {
            continue
        }

        cards += `
      <div class="card">
        <div class="ct">${title}</div>
        <table><tbody>${fieldRows}
        </tbody></table>
      </div>`
    }

    return `<!DOCTYPE html>
<html lang="en">
<head>
  <title>PAM Records Report</title>
  <meta name="author" content="Joe Linoff">
  <meta http-equiv="content-type" content="text/html; charset=utf-8" />
  <meta charset="utf-8">
  <meta name="viewport" content="width=device-width, initial-scale=1">
  <link rel="stylesheet" href="" id="x-print-report-css">
</head>
<body>
  <div class="cover">
    <h1>PAM Records Report</h1>
    <div class="meta">
      ${metaLine} &nbsp;&middot;&nbsp; Printed: ${sanitize(now)}
    </div>
    <div class="confidential">Confidential &mdash; Store securely</div>
  </div>

  <div class="grid">
    ${cards}
  </div>

  <div class="footer">
    PAM Records Report &nbsp;&middot;&nbsp; CONFIDENTIAL &nbsp;&middot;&nbsp; ${sanitize(now)}
  </div>


</body>
</html>`
}
